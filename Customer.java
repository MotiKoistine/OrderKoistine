
public class Customer {
	public int customerid;
	public String name, address, postcode, city, phone, email;
	Customer(int customerid,String name, String address,String postcode,String city,String phone,String email){
		this.customerid = customerid;
		this.name = name;
		this.address = address;
		this.postcode = postcode;
		this.city = city;
		this.phone = phone;
		this.email = email;
	}
	
}
